package com.abyssdev.entertheabyss.mapas;

public class SpawnPoint {
    public float x, y;
    public String name;
    public String salaId;

    public SpawnPoint(float x, float y, String name, String salaId) {
        this.x = x;
        this.y = y;
        this.name = name;
        this.salaId = salaId;
    }
}
